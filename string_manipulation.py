""" String manipulation """


string = "Hello Everyone"

print(string)

#length of string
print("length of string:",len(string))

#print string in upper case
print("upper case:",string.upper())

#print string in lower case
print("lower case:",string.lower())

#access 1st char of string
print("first char of string:",string[0])

#access last char of string
print("last char of string:",string[-1])

#slicing 4th to last char
print("4th to last char:",string[4:])
